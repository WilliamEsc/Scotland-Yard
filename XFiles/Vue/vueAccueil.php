	<h1 style="text-align: center">Bienvenue</h1>
	<h3>Objectif du site</h3>
	<p>L'objectif de ce site est de créer un jeu qui s'inspire du jeu de société Scotland Yard.</p>
	<h3>But du jeu</h3>
	<p>Où se trouve "Mister x" ?<br/>
Dans le centre de Lyon il n'est pas difficile de disparaître dans la cohue et la circulation.<br/>
Lorsque quelqu'un cherche à se rendre invisible, la perspicacité des célèbres détectives de Nautibus est nécessaire pour le dépister.<br/>
Un des joueurs est "Mister X" et se cache dans sa fuite dans tous les quartiers de Lyon. <br/>
Il joue en restant invisible et il ne se montre qu'à intervalles déterminés.<br/>
 Tous les autres joueurs sont les détectives de Nautibus et sont à sa poursuite pour le débusquer.</p><br/>
	<h3>Fin du jeu</h3>
<p>Le jeu est terminé, lorsqu'un des détectives arrive avec son pion sur le point où se trouve à ce moment "Mister X".<br/>
 Dans ce cas il doit se montrer. "Mister X" a donc perdu la partie.<br/>
Le jeu est également terminé, lorsque les détectives ne peuvent plus se déplacer. "Mister X" a donc gagné.<br/>
Ce cas se produit au plus tard avec le 22e coup, mais peut également arriver auparavant.<br/>
 En effet lorsqu'il ne reste aux détectives que des tickets qu'ils ne peuvent plus employer (bus, métro ),
  ils doivent rester sur le dernier point où ils sont arrivés.<br/>
  En cas de doute, on peut suivre les coups de "Mister X" pas à pas, d'après le tableau de parcours qu'il a tenu,<br/>
   en suivant les tickets remis. Les détectives peuvent également noter leurs propres cours afin de mieux suivre le jeu.<br/></p>
	
